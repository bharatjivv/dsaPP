int main(){
    int n;
    cout << "Enter the number you wanna check prime or not : ";
    cin >> n;
    bool isPrime = 1;
    for (int i = 2; i < n-1; i++)
    {
        if (n%i==0)
        {
            // cout << "Nos isn't prime ";
            isPrime = 0;
            break;
        }
    }
    
    if (isPrime == 0)
    {
        cout << "It's a Prime Nos";
    }
    else{
        cout << "Not a Prime Nos";
    }
    
}
